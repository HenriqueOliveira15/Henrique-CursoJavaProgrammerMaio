package entidades;

public abstract class Animal {
	
	private  String Nome; 
	private  String Caf;
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getCaf() {
		return Caf;
	}
	public void setCaf(String caf) {
		Caf = caf;
	}
	
	
	
	
	
	
}

